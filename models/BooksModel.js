class Books {
    constructor(id, categoryIds, name, genre, author, Pages, image,summary){
        this.id = id;
        this.categoryIds = categoryIds;
        this.name = name;
        this.genre = genre;
        this.author = author;
        this.Pages = Pages;
        this.image = image;
        this.summary = summary;
    }
}
export default Books;